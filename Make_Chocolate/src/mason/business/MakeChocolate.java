package mason.business;

public class MakeChocolate {
	
	private int small;
	private int big;
	private int goal;
	
	public MakeChocolate() {
		super();
	}

	public MakeChocolate(int small, int big, int goal) {
		super();
		this.small = small;
		this.big = big;
		this.goal = goal;
	}

	public int getSmall() {
		return small;
	}

	public void setSmall(int small) {
		this.small = small;
	}

	public int getBig() {
		return big;
	}

	public void setBig(int big) {
		this.big = big;
	}

	public int getGoal() {
		return goal;
	}

	public void setGoal(int goal) {
		this.goal = goal;
	}
	
	

}
